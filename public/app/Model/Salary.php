	public function get_individual_salary_list($id){
    $options = array(
      'conditions' => array(
        'Salary.employee_id' => $id
        ),
      'fields' => array(
        'id', 'month', 'basic', 'deposit', 'absence', 'late', 'early', 'overtime', 'off_work', 'bonus'
        ),
      'order' => array('month' => 'Desc')
      );
    $salaryList = $this->find('all', $options);
    return $salaryList;
  }
